import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

import { classifyParityScenarioState } from './conformance-parity-lib.mjs';

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const scenarioRegistry = JSON.parse(readFileSync(path.join(repoRoot, 'config', 'conformance-scenarios.json'), 'utf8'));
const parityStatusJsonPath = path.join(repoRoot, 'docs', 'generated', 'conformance-parity-status.json');
const parityStatusMarkdownPath = path.join(repoRoot, 'docs', 'generated', 'conformance-parity-status.md');

const filterId = process.argv[2] ?? null;
const selectedScenarios = filterId
  ? scenarioRegistry.filter((scenario) => scenario.id === filterId)
  : scenarioRegistry;

if (selectedScenarios.length === 0) {
  console.error(filterId ? `Unknown conformance scenario id: ${filterId}` : 'No conformance scenarios found.');
  process.exit(1);
}

const results = [];
for (const scenario of selectedScenarios) {
  const paritySpecPath = path.join(repoRoot, scenario.paritySpecPath);
  const paritySpec = JSON.parse(readFileSync(paritySpecPath, 'utf8'));

  const state = classifyParityScenarioState(scenario, paritySpec);

  results.push({
    scenarioId: scenario.id,
    operations: scenario.operationNames,
    scenarioStatus: scenario.status,
    paritySpecPath: scenario.paritySpecPath,
    state,
    assertionKinds: scenario.assertionKinds,
    captureFiles: scenario.captureFiles,
    blocker: paritySpec.blocker ?? null,
  });
}

const parityStatus = {
  generatedAt: new Date().toISOString(),
  ok: true,
  total: results.length,
  readyForComparison: results.filter((result) => result.state === 'ready-for-comparison').length,
  blocked: results.filter((result) => result.state.startsWith('blocked')).length,
  pending: results.filter((result) => result.state !== 'ready-for-comparison' && !result.state.startsWith('blocked')).length,
  results,
};

const parityStatusReport = [
  '# Conformance Parity Status',
  '',
  'Generated by `corepack pnpm conformance:parity`.',
  '',
  `- Total scenarios: ${parityStatus.total}`,
  `- Ready for comparison: ${parityStatus.readyForComparison}`,
  `- Blocked scenarios: ${parityStatus.blocked}`,
  `- Pending scenarios: ${parityStatus.pending}`,
  '',
  '## Scenario states',
  '',
  ...results.map((result) => {
    const blockerDetails = [];
    if (Array.isArray(result.blocker?.details?.requiredScopes) && result.blocker.details.requiredScopes.length > 0) {
      blockerDetails.push(
        `required scopes: ${result.blocker.details.requiredScopes.map((scope) => `\`${scope}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.probeRoots) && result.blocker.details.probeRoots.length > 0) {
      blockerDetails.push(
        `probe roots: ${result.blocker.details.probeRoots.map((root) => `\`${root}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.blockedFields) && result.blocker.details.blockedFields.length > 0) {
      blockerDetails.push(
        `blocked fields: ${result.blocker.details.blockedFields.map((field) => `\`${field}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.grantedScopeHandles) && result.blocker.details.grantedScopeHandles.length > 0) {
      blockerDetails.push(
        `granted scopes on active token: ${result.blocker.details.grantedScopeHandles.map((scope) => `\`${scope}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.blockedMutations) && result.blocker.details.blockedMutations.length > 0) {
      blockerDetails.push(
        `blocked mutations: ${result.blocker.details.blockedMutations.map((mutation) => `\`${mutation}\``).join(', ')}`,
      );
    }
    if (typeof result.blocker?.details?.requiredTokenMode === 'string' && result.blocker.details.requiredTokenMode.length > 0) {
      blockerDetails.push(`required token mode: \`${result.blocker.details.requiredTokenMode}\``);
    }
    if (Array.isArray(result.blocker?.details?.requiredPermissions) && result.blocker.details.requiredPermissions.length > 0) {
      blockerDetails.push(
        `required permissions: ${result.blocker.details.requiredPermissions.map((permission) => `\`${permission}\``).join(', ')}`,
      );
    }
    if (typeof result.blocker?.details?.appHandle === 'string' && result.blocker.details.appHandle.length > 0) {
      const configuredAppLabel = typeof result.blocker.details.appId === 'string' && result.blocker.details.appId.length > 0
        ? `configured app: \`${result.blocker.details.appHandle}\` (\`${result.blocker.details.appId}\`)`
        : `configured app: \`${result.blocker.details.appHandle}\``;
      blockerDetails.push(configuredAppLabel);
    }
    if (typeof result.blocker?.details?.appConfigPath === 'string' && result.blocker.details.appConfigPath.length > 0) {
      blockerDetails.push(`app config: \`${result.blocker.details.appConfigPath}\``);
    }
    if (typeof result.blocker?.details?.publicationTargetStatus === 'string' && result.blocker.details.publicationTargetStatus.length > 0) {
      const publicationTargetLabel = typeof result.blocker.details.publicationTargetMessage === 'string'
        && result.blocker.details.publicationTargetMessage.length > 0
        ? `publication target: \`${result.blocker.details.publicationTargetStatus}\` — ${result.blocker.details.publicationTargetMessage}`
        : `publication target: \`${result.blocker.details.publicationTargetStatus}\``;
      blockerDetails.push(publicationTargetLabel);
    }
    if (typeof result.blocker?.details?.shopifyAppCliAuthStatus === 'string' && result.blocker.details.shopifyAppCliAuthStatus.length > 0) {
      const shopifyAppCliAuthLabel = typeof result.blocker.details.shopifyAppCliAuthWorkdir === 'string'
        && result.blocker.details.shopifyAppCliAuthWorkdir.length > 0
        ? `Shopify app CLI auth: \`${result.blocker.details.shopifyAppCliAuthStatus}\` @ \`${result.blocker.details.shopifyAppCliAuthWorkdir}\``
        : `Shopify app CLI auth: \`${result.blocker.details.shopifyAppCliAuthStatus}\``;
      blockerDetails.push(shopifyAppCliAuthLabel);
    }
    if (typeof result.blocker?.details?.shopifyAppDeployStatus === 'string' && result.blocker.details.shopifyAppDeployStatus.length > 0) {
      const versionSuffix = typeof result.blocker.details.shopifyAppDeployVersion === 'string'
        && result.blocker.details.shopifyAppDeployVersion.length > 0
        ? ` (\`${result.blocker.details.shopifyAppDeployVersion}\`)`
        : '';
      const shopifyAppDeployLabel = typeof result.blocker.details.shopifyAppDeployCommand === 'string'
        && result.blocker.details.shopifyAppDeployCommand.length > 0
        ? `Shopify app deploy: \`${result.blocker.details.shopifyAppDeployStatus}\` via \`${result.blocker.details.shopifyAppDeployCommand}\`${versionSuffix}`
        : `Shopify app deploy: \`${result.blocker.details.shopifyAppDeployStatus}\`${versionSuffix}`;
      blockerDetails.push(shopifyAppDeployLabel);
    }
    if (typeof result.blocker?.details?.channelConfigHandle === 'string' && result.blocker.details.channelConfigHandle.length > 0) {
      const channelConfigLabel = typeof result.blocker.details.channelConfigExtensionPath === 'string'
        && result.blocker.details.channelConfigExtensionPath.length > 0
        ? `channel config extension: \`${result.blocker.details.channelConfigHandle}\` @ \`${result.blocker.details.channelConfigExtensionPath}\``
        : `channel config extension: \`${result.blocker.details.channelConfigHandle}\``;
      blockerDetails.push(channelConfigLabel);
    }
    if (typeof result.blocker?.details?.channelConfigCreateLegacyChannelOnAppInstall === 'boolean') {
      blockerDetails.push(
        `createLegacyChannelOnAppInstall: \`${String(result.blocker.details.channelConfigCreateLegacyChannelOnAppInstall)}\``,
      );
    }
    if (typeof result.blocker?.details?.publicationTargetRemediation === 'string' && result.blocker.details.publicationTargetRemediation.length > 0) {
      blockerDetails.push(`publication remediation: \`${result.blocker.details.publicationTargetRemediation}\``);
    }
    if (typeof result.blocker?.details?.activeCredentialTokenFamily === 'string' && result.blocker.details.activeCredentialTokenFamily.length > 0) {
      blockerDetails.push(`credential family: \`${result.blocker.details.activeCredentialTokenFamily}\``);
    }
    if (typeof result.blocker?.details?.activeCredentialHeaderMode === 'string' && result.blocker.details.activeCredentialHeaderMode.length > 0) {
      blockerDetails.push(`credential header mode: \`${result.blocker.details.activeCredentialHeaderMode}\``);
    }
    if (typeof result.blocker?.details?.activeCredentialSummary === 'string' && result.blocker.details.activeCredentialSummary.length > 0) {
      blockerDetails.push(`credential summary: ${result.blocker.details.activeCredentialSummary}`);
    }
    if (typeof result.blocker?.details?.manualStoreAuthStatus === 'string' && result.blocker.details.manualStoreAuthStatus.length > 0) {
      const manualStoreAuthLabel = typeof result.blocker.details.manualStoreAuthTokenPath === 'string'
        && result.blocker.details.manualStoreAuthTokenPath.length > 0
        ? `manual store auth: \`${result.blocker.details.manualStoreAuthStatus}\` @ \`${result.blocker.details.manualStoreAuthTokenPath}\``
        : `manual store auth: \`${result.blocker.details.manualStoreAuthStatus}\``;
      blockerDetails.push(manualStoreAuthLabel);
    }
    if (Array.isArray(result.blocker?.details?.manualStoreAuthScopes) && result.blocker.details.manualStoreAuthScopes.length > 0) {
      blockerDetails.push(
        `manual store auth live scopes: ${result.blocker.details.manualStoreAuthScopes.map((scope) => `\`${scope}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.manualStoreAuthCachedScopes) && result.blocker.details.manualStoreAuthCachedScopes.length > 0) {
      blockerDetails.push(
        `manual store auth cached scopes: ${result.blocker.details.manualStoreAuthCachedScopes.map((scope) => `\`${scope}\``).join(', ')}`,
      );
    }
    if (Array.isArray(result.blocker?.details?.manualStoreAuthAssociatedUserScopes) && result.blocker.details.manualStoreAuthAssociatedUserScopes.length > 0) {
      blockerDetails.push(
        `manual store auth associated user scopes: ${result.blocker.details.manualStoreAuthAssociatedUserScopes.map((scope) => `\`${scope}\``).join(', ')}`,
      );
    }

    const blockerSummary = result.blocker
      ? ` — blocker: \`${result.blocker.kind}\` @ \`${result.blocker.blockerPath}\`${blockerDetails.length > 0 ? ` (${blockerDetails.join('; ')})` : ''}`
      : '';
    return `- \`${result.scenarioId}\` (${result.operations.map((operation) => `\`${operation}\``).join(', ')}) → \`${result.state}\`${blockerSummary}`;
  }),
  '',
];

mkdirSync(path.dirname(parityStatusJsonPath), { recursive: true });
writeFileSync(parityStatusJsonPath, `${JSON.stringify(parityStatus, null, 2)}\n`);
writeFileSync(parityStatusMarkdownPath, parityStatusReport.join('\n'));

console.log(JSON.stringify(parityStatus, null, 2));
