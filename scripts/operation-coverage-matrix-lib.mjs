import { readFileSync } from 'node:fs';
import path from 'node:path';

import { classifyParityScenarioState } from './conformance-parity-lib.mjs';

function compareScenarioIds(left, right) {
  return left.localeCompare(right);
}

function buildScenarioStateMap({ repoRoot, scenarioRegistry }) {
  const stateByScenarioId = new Map();

  for (const scenario of scenarioRegistry) {
    const paritySpecPath = path.join(repoRoot, scenario.paritySpecPath);
    const paritySpec = JSON.parse(readFileSync(paritySpecPath, 'utf8'));
    const state = classifyParityScenarioState(scenario, paritySpec);

    stateByScenarioId.set(scenario.id, {
      id: scenario.id,
      operationNames: scenario.operationNames,
      status: scenario.status,
      assertionKinds: scenario.assertionKinds,
      paritySpecPath: scenario.paritySpecPath,
      captureFiles: scenario.captureFiles,
      state,
      blocker: paritySpec.blocker ?? null,
    });
  }

  return stateByScenarioId;
}

function summarizeOperationScenarios(scenarios) {
  const readyScenarioCount = scenarios.filter((scenario) => scenario.state === 'ready-for-comparison').length;
  const blockedScenarioCount = scenarios.filter((scenario) => scenario.state.startsWith('blocked')).length;
  const pendingScenarioCount = scenarios.filter(
    (scenario) => scenario.state !== 'ready-for-comparison' && !scenario.state.startsWith('blocked'),
  ).length;

  return {
    readyScenarioCount,
    blockedScenarioCount,
    pendingScenarioCount,
    hasReadyScenario: readyScenarioCount > 0,
    hasBlockedScenario: blockedScenarioCount > 0,
    hasPendingScenario: pendingScenarioCount > 0,
  };
}

export function buildOperationCoverageMatrix({ repoRoot, operationRegistry, scenarioRegistry }) {
  const allEntries = operationRegistry;
  const coveredEntries = allEntries.filter((entry) => entry.conformance.status === 'covered');
  const gapEntries = allEntries.filter((entry) => entry.conformance.status === 'declared-gap');
  const stateByScenarioId = buildScenarioStateMap({ repoRoot, scenarioRegistry });
  const scenarioStates = Array.from(stateByScenarioId.values()).sort((left, right) => compareScenarioIds(left.id, right.id));

  const operations = allEntries.map((entry) => {
    const declaredScenarioIds = new Set(entry.conformance.scenarioIds ?? []);
    const scenarios = scenarioStates
      .filter((scenario) => scenario.operationNames.includes(entry.name))
      .map((scenario) => ({
        ...scenario,
        declaredOnOperation: declaredScenarioIds.has(scenario.id),
      }))
      .sort((left, right) => compareScenarioIds(left.id, right.id));

    return {
      name: entry.name,
      type: entry.type,
      execution: entry.execution,
      conformanceStatus: entry.conformance.status,
      declaredGapReason: entry.conformance.reason ?? null,
      scenarioIds: entry.conformance.scenarioIds ?? [],
      assertionKinds: Array.from(new Set(scenarios.flatMap((scenario) => scenario.assertionKinds))).sort(compareScenarioIds),
      parity: summarizeOperationScenarios(scenarios),
      scenarios,
    };
  });

  const blockedOperations = operations.filter((operation) => operation.parity.hasBlockedScenario);
  const pendingOperations = operations.filter((operation) => operation.parity.hasPendingScenario);

  return {
    generatedAt: new Date().toISOString(),
    totals: {
      operations: operations.length,
      coveredOperations: coveredEntries.length,
      declaredGaps: gapEntries.length,
      scenarios: scenarioStates.length,
      readyScenarios: scenarioStates.filter((scenario) => scenario.state === 'ready-for-comparison').length,
      blockedScenarios: scenarioStates.filter((scenario) => scenario.state.startsWith('blocked')).length,
      pendingScenarios: scenarioStates.filter(
        (scenario) => scenario.state !== 'ready-for-comparison' && !scenario.state.startsWith('blocked'),
      ).length,
      blockedOperations: blockedOperations.length,
      pendingOperations: pendingOperations.length,
    },
    blockedOperationNames: blockedOperations.map((operation) => operation.name),
    pendingOperationNames: pendingOperations.map((operation) => operation.name),
    operations,
  };
}

export function renderOperationCoverageMatrixMarkdown(matrix) {
  const lines = [
    '# Operation Coverage Matrix',
    '',
    'Generated by `corepack pnpm conformance:check`.',
    '',
    `- Operations: ${matrix.totals.operations}`,
    `- Covered operations: ${matrix.totals.coveredOperations}`,
    `- Declared gaps: ${matrix.totals.declaredGaps}`,
    `- Ready scenarios: ${matrix.totals.readyScenarios}`,
    `- Blocked scenarios: ${matrix.totals.blockedScenarios}`,
    `- Pending scenarios: ${matrix.totals.pendingScenarios}`,
    `- Blocked operations: ${matrix.totals.blockedOperations}`,
    `- Pending operations: ${matrix.totals.pendingOperations}`,
    '',
    '## Blocked operations',
    '',
  ];

  if (matrix.blockedOperationNames.length === 0) {
    lines.push('None.', '');
  } else {
    for (const operation of matrix.operations.filter((candidate) => candidate.parity.hasBlockedScenario)) {
      const blockerKinds = Array.from(
        new Set(operation.scenarios.flatMap((scenario) => (scenario.blocker?.kind ? [scenario.blocker.kind] : []))),
      );
      const blockerPaths = Array.from(
        new Set(operation.scenarios.flatMap((scenario) => (scenario.blocker?.blockerPath ? [scenario.blocker.blockerPath] : []))),
      );
      lines.push(
        `- \`${operation.name}\` → ${blockerKinds.map((kind) => `\`${kind}\``).join(', ')}${blockerPaths.length > 0 ? ` @ ${blockerPaths.map((blockerPath) => `\`${blockerPath}\``).join(', ')}` : ''}`,
      );
    }
    lines.push('');
  }

  lines.push('## Operations', '');
  for (const operation of matrix.operations) {
    lines.push(
      `- \`${operation.name}\` (${operation.type}, ${operation.execution}) — conformance: \`${operation.conformanceStatus}\`; scenarios ready/blocked/pending: ${operation.parity.readyScenarioCount}/${operation.parity.blockedScenarioCount}/${operation.parity.pendingScenarioCount}`,
    );
    if (operation.assertionKinds.length > 0) {
      lines.push(`  - assertion kinds: ${operation.assertionKinds.map((kind) => `\`${kind}\``).join(', ')}`);
    }
    for (const scenario of operation.scenarios) {
      const blockerSummary = scenario.blocker
        ? ` — blocker: \`${scenario.blocker.kind}\` @ \`${scenario.blocker.blockerPath}\``
        : '';
      lines.push(`  - scenario \`${scenario.id}\` → \`${scenario.state}\`${blockerSummary}`);
    }
  }
  lines.push('');

  return lines.join('\n');
}
