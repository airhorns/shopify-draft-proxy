use crate::graphql::OperationType;
use crate::operation_registry::{CapabilityDomain, CapabilityExecution, OperationRegistryEntry};

macro_rules! entry {
    (
        $name:literal,
        $operation_type:ident,
        $domain:ident,
        $execution:ident,
        $implemented:literal,
        [$($runtime_test:literal),* $(,)?]
    ) => {
        registry_entry(
            $name,
            OperationType::$operation_type,
            CapabilityDomain::$domain,
            CapabilityExecution::$execution,
            $implemented,
            &[$($runtime_test),*],
        )
    };
}

macro_rules! entry_aliased {
    (
        $name:literal,
        $operation_type:ident,
        $domain:ident,
        $execution:ident,
        $implemented:literal,
        [$($match_name:literal),* $(,)?],
        [$($runtime_test:literal),* $(,)?]
    ) => {
        registry_entry_aliased(
            $name,
            OperationType::$operation_type,
            CapabilityDomain::$domain,
            CapabilityExecution::$execution,
            $implemented,
            &[$($match_name),*],
            &[$($runtime_test),*],
        )
    };
}

